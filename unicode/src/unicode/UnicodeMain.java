package unicode;

public class UnicodeMain {

	public static void main(String[] args) {
		System.out.println("x" + '\u00b2');
		System.out.println("x" + '\u00b3');
		System.out.println("x" + "ʸ");
		System.out.println("e" + "ˣ");
		System.out.println("10" + '\u207f');
		System.out.println("log" + '\u2081' + '\u2080');
		System.out.println("2" + "ⁿᵈ");
		System.out.println('\u215f' + "x");
		System.out.println("Π");
		System.out.println("2" + '\u221A' + 'x');
		System.out.println("3" + '\u221A' + 'x');
		System.out.println("y" + '\u221A' + 'x');
	}
}
